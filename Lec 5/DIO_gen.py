#program file
DIO_prg.write("#include \"types.h\"\n#include \"BIT_MATH.h\"\n#include \"DIO_interface.h\"\n#include \"DIO_priv.h\"\nvoid DIO_vidSetPinVal(u8 PortId, u8 PinId, u8 val)\n{\n	//check the entered value is within valid range\n	if(PortId <= DIO_PORTD && PinId <= DIO_PIN7)\n	{\n		if (val == DIO_LOW)\n		{\n			switch(PortId)\n			{\n			case DIO_PORTA: CLR_BIT(PORTA_REG,PinId); break;\n			case DIO_PORTB: CLR_BIT(PORTB_REG,PinId); break;\n			case DIO_PORTC: CLR_BIT(PORTC_REG,PinId); break;\n			case DIO_PORTD: CLR_BIT(PORTD_REG,PinId); break;\n			}\n		}\n		else if(val == DIO_HIGH)\n		{\n			switch(PortId)\n			{\n			case DIO_PORTA: SET_BIT(PORTA_REG,PinId); break;\n			case DIO_PORTB: SET_BIT(PORTB_REG,PinId); break;\n			case DIO_PORTC: SET_BIT(PORTC_REG,PinId); break;\n			case DIO_PORTD: SET_BIT(PORTD_REG,PinId); break;\n			}\n		}\n		else\n		{\n			//report error\n		}\n	}\n	else\n	{\n		//report error\n	}\n}\nvoid DIO_vidSetPinDir(u8 PortId, u8 PinId, u8 dir)\n{\n	//check the entered value is within valid range\n	if(PortId <= DIO_PORTD && PinId <= DIO_PIN7)\n	{\n		if (dir == DIO_INPUT)\n		{\n			switch(PortId)\n			{\n			case DIO_PORTA: CLR_BIT(DDRA_REG,PinId); break;\n			case DIO_PORTB: CLR_BIT(DDRB_REG,PinId); break;\n			case DIO_PORTC: CLR_BIT(DDRC_REG,PinId); break;\n			case DIO_PORTD: CLR_BIT(DDRD_REG,PinId); break;\n			}\n		}\n		else if(dir == DIO_OUTPUT)\n		{\n			switch(PortId)\n			{\n			case DIO_PORTA: SET_BIT(DDRA_REG,PinId); break;\n			case DIO_PORTB: SET_BIT(DDRB_REG,PinId); break;\n			case DIO_PORTC: SET_BIT(DDRC_REG,PinId); break;\n			case DIO_PORTD: SET_BIT(DDRD_REG,PinId); break;\n			}\n		}\n		else\n		{\n			//report error\n		}\n	}\n	else\n	{\n		//report error\n	}\n}\nvoid DIO_vidSetPortVal(u8 PortId, u8 val)\n{\n	if(PortId <= DIO_PORTD)\n	{\n		switch(PortId)\n		{\n		case DIO_PORTA: PORTA_REG=val; break;\n		case DIO_PORTB: PORTB_REG=val; break;\n		case DIO_PORTC: PORTC_REG=val; break;\n		case DIO_PORTD: PORTD_REG=val; break;\n		}\n	}\n	else\n	{\n		//report error\n	}\n}\nvoid DIO_vidSetPortDir(u8 PortId, u8 dir)\n{\n	if(PortId <= DIO_PORTD)\n		{\n			switch(PortId)\n			{\n			case DIO_PORTA: DDRA_REG=dir; break;\n			case DIO_PORTB: DDRB_REG=dir; break;\n			case DIO_PORTC: DDRC_REG=dir; break;\n			case DIO_PORTD: DDRD_REG=dir; break;\n			}\n		}\n		else\n		{\n			//report error\n		}\n}\nu8 DIO_u8GetPinVal(u8 PortId, u8 PinId)\n{\n	u8 ret_val;\n	if(PortId <= DIO_PORTD && PinId <= DIO_PIN7)\n	{\n		switch(PortId)\n		{\n		case DIO_PORTA: ret_val=GET_BIT(PINA_REG, PinId); break;\n		case DIO_PORTB: ret_val=GET_BIT(PINB_REG, PinId); break;\n		case DIO_PORTC: ret_val=GET_BIT(PINC_REG, PinId); break;\n		case DIO_PORTD: ret_val=GET_BIT(PIND_REG, PinId); break;\n		}\n	}\n	else\n	{\n		//report error\n	}\n	return ret_val;\n}\n")
